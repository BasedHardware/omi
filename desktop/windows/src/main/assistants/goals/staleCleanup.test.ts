import { describe, it, expect, vi } from 'vitest'

vi.mock('electron', () => ({ net: { fetch: vi.fn() } }))
vi.mock('../core/session', () => ({
  getBackendSession: () => null,
  getAbortSignal: () => undefined
}))
vi.mock('../../appSettings', () => ({ getAppSettings: vi.fn(), setAppSettings: vi.fn() }))

import { removeStaleGoalsWith, type StaleCleanupDeps, type StaleGoal } from './staleCleanup'
import type { GoalAutoGeneratedRecord } from '../../appSettings'

const DAY = 24 * 60 * 60 * 1000
const NOW = 10 * 365 * DAY // a fixed "now" well past epoch

function deps(over: Partial<StaleCleanupDeps> = {}): StaleCleanupDeps & {
  deleteGoal: ReturnType<typeof vi.fn>
  setAttribution: ReturnType<typeof vi.fn>
} {
  const base = {
    listGoals: async () => [] as StaleGoal[],
    deleteGoal: vi.fn(async () => {}),
    getAttribution: () => [] as GoalAutoGeneratedRecord[],
    setAttribution: vi.fn(),
    now: NOW
  }
  return { ...base, ...over } as StaleCleanupDeps & {
    deleteGoal: ReturnType<typeof vi.fn>
    setAttribution: ReturnType<typeof vi.fn>
  }
}

describe('removeStaleGoalsWith', () => {
  it('does nothing when the attribution record is empty (no user goal is ever touched)', async () => {
    const d = deps({
      getAttribution: () => [],
      // Even with a stale-looking active goal present, it is NOT in the set → safe.
      listGoals: async () => [{ id: 'user_goal', currentValue: 0, updatedAt: NOW - 30 * DAY }]
    })
    expect(await removeStaleGoalsWith(d)).toEqual({ deleted: [] })
    expect(d.deleteGoal).not.toHaveBeenCalled()
  })

  it('deletes an attributed goal that is active, zero-progress, and ≥3 days old', async () => {
    const d = deps({
      getAttribution: () => [{ id: 'ai_1', createdAt: NOW - 5 * DAY }],
      listGoals: async () => [{ id: 'ai_1', currentValue: 0, updatedAt: NOW - 4 * DAY }]
    })
    expect(await removeStaleGoalsWith(d)).toEqual({ deleted: ['ai_1'] })
    expect(d.deleteGoal).toHaveBeenCalledWith('ai_1')
    // The deleted id is pruned from the attribution record.
    expect(d.setAttribution).toHaveBeenCalledWith([])
  })

  it('never deletes a user-created goal (absent from the attribution set)', async () => {
    const d = deps({
      getAttribution: () => [{ id: 'ai_1', createdAt: NOW - 5 * DAY }],
      // The user goal is old + zero-progress, but not attributed → must survive.
      listGoals: async () => [
        { id: 'user_goal', currentValue: 0, updatedAt: NOW - 40 * DAY },
        { id: 'ai_1', currentValue: 0, updatedAt: NOW - 4 * DAY }
      ]
    })
    await removeStaleGoalsWith(d)
    expect(d.deleteGoal).toHaveBeenCalledTimes(1)
    expect(d.deleteGoal).toHaveBeenCalledWith('ai_1')
    expect(d.deleteGoal).not.toHaveBeenCalledWith('user_goal')
  })

  it('keeps a fresh attributed goal (younger than 3 days)', async () => {
    const d = deps({
      getAttribution: () => [{ id: 'ai_1', createdAt: NOW - 1 * DAY }],
      listGoals: async () => [{ id: 'ai_1', currentValue: 0, updatedAt: NOW - 1 * DAY }]
    })
    expect(await removeStaleGoalsWith(d)).toEqual({ deleted: [] })
    expect(d.deleteGoal).not.toHaveBeenCalled()
  })

  it('keeps an attributed goal that has made progress', async () => {
    const d = deps({
      getAttribution: () => [{ id: 'ai_1', createdAt: NOW - 10 * DAY }],
      listGoals: async () => [{ id: 'ai_1', currentValue: 3, updatedAt: NOW - 10 * DAY }]
    })
    expect(await removeStaleGoalsWith(d)).toEqual({ deleted: [] })
    expect(d.deleteGoal).not.toHaveBeenCalled()
  })

  it('falls back to the record createdAt for age when the goal has no updated_at', async () => {
    const d = deps({
      getAttribution: () => [{ id: 'ai_1', createdAt: NOW - 5 * DAY }],
      listGoals: async () => [{ id: 'ai_1', currentValue: 0, updatedAt: null }]
    })
    expect(await removeStaleGoalsWith(d)).toEqual({ deleted: ['ai_1'] })
  })

  it('does not wipe attribution when listGoals fails open to [] (no orphaning)', async () => {
    const d = deps({
      getAttribution: () => [{ id: 'ai_1', createdAt: NOW - 5 * DAY }],
      listGoals: async () => [] // transient failure surfaces as empty
    })
    expect(await removeStaleGoalsWith(d)).toEqual({ deleted: [] })
    expect(d.deleteGoal).not.toHaveBeenCalled()
    // The record must be preserved so the real goal is still tracked next run.
    expect(d.setAttribution).not.toHaveBeenCalled()
  })

  it('keeps the id (for retry) when the delete call fails', async () => {
    const d = deps({
      getAttribution: () => [{ id: 'ai_1', createdAt: NOW - 5 * DAY }],
      listGoals: async () => [{ id: 'ai_1', currentValue: 0, updatedAt: NOW - 4 * DAY }],
      deleteGoal: vi.fn(async () => {
        throw new Error('HTTP 500')
      })
    })
    expect(await removeStaleGoalsWith(d)).toEqual({ deleted: [] })
    expect(d.setAttribution).not.toHaveBeenCalled()
  })
})
