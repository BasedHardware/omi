import { describe, it, expect, vi } from 'vitest'

// generate.ts imports electron + the session/notify/appSettings singletons only
// for its REAL side-effects. The unit tests here drive the pure helpers and the
// injected-deps flow, so the singletons are stubbed inert.
vi.mock('electron', () => ({ net: { fetch: vi.fn() }, BrowserWindow: { getAllWindows: () => [] } }))
vi.mock('../core/session', () => ({
  getBackendSession: () => null,
  getSessionEpoch: () => 0,
  getAbortSignal: () => undefined
}))
vi.mock('../core/notify', () => ({ notifyProactive: vi.fn() }))
vi.mock('../../appSettings', () => ({
  getAppSettings: () => ({ goalAutoGeneratedIds: [] }),
  setAppSettings: vi.fn()
}))

import {
  buildCreateBody,
  parseGoalSuggestion,
  runGoalGenerationWith,
  validateLinkedTaskIds,
  type GenerateDeps,
  type GoalSuggestion
} from './generate'
import type { GoalContextData } from './context'

const suggestion = (over: Partial<GoalSuggestion> = {}): GoalSuggestion => ({
  title: 'Read 12 books',
  description: 'Grow through reading.',
  type: 'numeric',
  target: 12,
  min: 0,
  max: 12,
  reasoning: 'Fits their learning goals.',
  linkedTaskIds: [],
  ...over
})

const context = (over: Partial<GoalContextData> = {}): GoalContextData => ({
  persona: null,
  memories: ['likes learning'],
  conversations: [],
  tasks: [],
  activeGoals: [],
  completedGoals: [],
  activeGoalCount: 0,
  ...over
})

describe('parseGoalSuggestion', () => {
  const full = JSON.stringify({
    suggested_title: 'Read 12 books',
    suggested_description: 'Grow through reading.',
    suggested_type: 'numeric',
    suggested_target: 12,
    suggested_min: 0,
    suggested_max: 12,
    reasoning: 'Fits their learning goals.',
    linked_task_ids: ['a1', 'b2']
  })

  it('parses a valid suggestion', () => {
    expect(parseGoalSuggestion(full)).toEqual({
      title: 'Read 12 books',
      description: 'Grow through reading.',
      type: 'numeric',
      target: 12,
      min: 0,
      max: 12,
      reasoning: 'Fits their learning goals.',
      linkedTaskIds: ['a1', 'b2']
    })
  })

  it('returns null on empty / unparseable / missing title / non-numeric target', () => {
    expect(parseGoalSuggestion('')).toBeNull()
    expect(parseGoalSuggestion('not json')).toBeNull()
    expect(parseGoalSuggestion(JSON.stringify({ suggested_target: 5 }))).toBeNull() // no title
    expect(
      parseGoalSuggestion(JSON.stringify({ suggested_title: 'x', suggested_target: 'lots' }))
    ).toBeNull()
  })

  it('coerces a non-enum type to numeric and defaults min/max', () => {
    const s = parseGoalSuggestion(
      JSON.stringify({ suggested_title: 'x', suggested_type: 'weird', suggested_target: 7 })
    )
    expect(s?.type).toBe('numeric')
    expect(s?.min).toBe(0)
    expect(s?.max).toBe(7) // defaults to target
  })

  it('drops non-string / non-array linked_task_ids', () => {
    expect(
      parseGoalSuggestion(
        JSON.stringify({ suggested_title: 'x', suggested_target: 1, linked_task_ids: ['a', 2, ''] })
      )?.linkedTaskIds
    ).toEqual(['a'])
    expect(
      parseGoalSuggestion(
        JSON.stringify({ suggested_title: 'x', suggested_target: 1, linked_task_ids: 'a1' })
      )?.linkedTaskIds
    ).toEqual([])
  })
})

describe('buildCreateBody (422-guard)', () => {
  it('always sends a positive target_value + source', () => {
    const body = buildCreateBody(suggestion({ target: 12 }))
    expect(body.target_value).toBe(12)
    expect(body.source).toBe('ai_suggested')
    expect(body.current_value).toBe(0)
    expect(body.goal_type).toBe('numeric')
  })

  it('defaults a non-positive / NaN target to 1', () => {
    expect(buildCreateBody(suggestion({ target: 0 })).target_value).toBe(1)
    expect(buildCreateBody(suggestion({ target: -5 })).target_value).toBe(1)
    expect(buildCreateBody(suggestion({ target: NaN })).target_value).toBe(1)
  })

  it('falls back max to target when max is non-positive', () => {
    expect(buildCreateBody(suggestion({ target: 8, max: 0 })).max_value).toBe(8)
  })
})

describe('validateLinkedTaskIds', () => {
  it('keeps only ids present in the fetched bundle', () => {
    const s = suggestion({ linkedTaskIds: ['a1', 'ghost', 'b2'] })
    expect(validateLinkedTaskIds(s, ['a1', 'b2', 'c3'])).toEqual(['a1', 'b2'])
    expect(validateLinkedTaskIds(s, [])).toEqual([])
  })
})

describe('runGoalGenerationWith', () => {
  function deps(over: Partial<GenerateDeps> = {}): GenerateDeps {
    return {
      getContext: async () => context(),
      generate: async () => JSON.stringify({ suggested_title: 'G', suggested_target: 5 }),
      createGoal: vi.fn(async () => 'goal_new'),
      recordAttribution: vi.fn(),
      linkTasks: vi.fn(async () => {}),
      notify: vi.fn(),
      broadcastChanged: vi.fn(),
      epochAtEntry: 1,
      currentEpoch: () => 1,
      ...over
    }
  }

  it('skips when there is no session (null context)', async () => {
    const d = deps({ getContext: async () => null })
    expect(await runGoalGenerationWith(d)).toEqual({ status: 'skipped', reason: 'no_session' })
    expect(d.createGoal).not.toHaveBeenCalled()
  })

  it('skips on insufficient context', async () => {
    const d = deps({
      getContext: async () => context({ memories: [], conversations: [], tasks: [] })
    })
    expect(await runGoalGenerationWith(d)).toEqual({
      status: 'skipped',
      reason: 'insufficient_context'
    })
    expect(d.createGoal).not.toHaveBeenCalled()
  })

  it('skips on an unparseable model response', async () => {
    const d = deps({ generate: async () => 'garbage' })
    expect(await runGoalGenerationWith(d)).toEqual({
      status: 'skipped',
      reason: 'invalid_suggestion'
    })
    expect(d.createGoal).not.toHaveBeenCalled()
  })

  it('discards the run (never creates) when the session epoch changed', async () => {
    const d = deps({ epochAtEntry: 1, currentEpoch: () => 2 })
    expect(await runGoalGenerationWith(d)).toEqual({ status: 'skipped', reason: 'stale' })
    expect(d.createGoal).not.toHaveBeenCalled()
  })

  it('creates the goal, records attribution, notifies, and broadcasts', async () => {
    const d = deps()
    const result = await runGoalGenerationWith(d)
    expect(result).toEqual({ status: 'created', goalId: 'goal_new', title: 'G' })
    // 422-guard: the POST body always carries target_value.
    expect((d.createGoal as ReturnType<typeof vi.fn>).mock.calls[0][0]).toMatchObject({
      title: 'G',
      target_value: 5,
      source: 'ai_suggested'
    })
    expect(d.recordAttribution).toHaveBeenCalledWith('goal_new')
    expect(d.notify).toHaveBeenCalled()
    expect(d.broadcastChanged).toHaveBeenCalled()
  })

  it('only links task ids that exist in the bundle', async () => {
    const d = deps({
      getContext: async () => context({ tasks: [{ id: 'a1', description: 'real' }] }),
      generate: async () =>
        JSON.stringify({
          suggested_title: 'G',
          suggested_target: 5,
          linked_task_ids: ['a1', 'ghost']
        })
    })
    await runGoalGenerationWith(d)
    expect(d.linkTasks).toHaveBeenCalledWith('goal_new', ['a1'])
  })

  it('does not link when no valid ids remain', async () => {
    const d = deps({
      generate: async () =>
        JSON.stringify({ suggested_title: 'G', suggested_target: 5, linked_task_ids: ['ghost'] })
    })
    await runGoalGenerationWith(d)
    expect(d.linkTasks).not.toHaveBeenCalled()
  })

  it('still succeeds when linking throws', async () => {
    const d = deps({
      getContext: async () => context({ tasks: [{ id: 'a1', description: 'real' }] }),
      generate: async () =>
        JSON.stringify({ suggested_title: 'G', suggested_target: 5, linked_task_ids: ['a1'] }),
      linkTasks: vi.fn(async () => {
        throw new Error('no endpoint')
      })
    })
    expect((await runGoalGenerationWith(d)).status).toBe('created')
  })
})
