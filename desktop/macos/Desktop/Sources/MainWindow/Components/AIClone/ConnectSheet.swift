        let venvPython = (Bundle.main.bundlePath as NSString)
            .appendingPathComponent("Contents/Resources/plugins/telegram-user-account/.venv/bin/python3")
        if FileManager.default.fileExists(atPath: venvPython) {
            return []
        }
        return ["python3"]
    }

