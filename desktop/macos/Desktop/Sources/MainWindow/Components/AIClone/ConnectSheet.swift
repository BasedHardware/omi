    /// Launch session_string_generator.py as a subprocess. The
    /// subprocess prompts for api_id/api_hash/phone/code in the
    /// user's terminal (we pass through stdin/stdout/stderr).
    /// On exit code 0, the LAST line of stdout is the session
    /// string -- we capture it and write to Keychain via
    /// config.setTelegramUserSession().
    ///
    /// SECURITY (plan §7): the captured session string is
    /// written directly to Keychain. It is NEVER logged. It is
    /// NEVER included in `sessionGeneratorOutput` AFTER the
    /// Keychain write succeeds -- we clear the in-memory copy
    /// immediately. The `sessionGeneratorOutput` @State stays
    /// empty on the happy path.
    private func startSessionGeneration() {
        guard !generatingSession else { return }
        generatingSession = true
        sessionGeneratorError = nil
        sessionGeneratorOutput = ""
        let task = Task.detached { [pythonExecutable, sessionGeneratorArgs] in
            await self.runSessionGenerator(
                executable: pythonExecutable,
                baseArgs: sessionGeneratorArgs,
            )
        }
        sessionGeneratorOutput = ""  // placeholder
        _ = task
    }

    private func cancelSessionGeneration() {
        sessionGeneratorProcess?.terminate()
        sessionGeneratorProcess = nil
        generatingSession = false
        sessionGeneratorError = "Cancelled."
    }

