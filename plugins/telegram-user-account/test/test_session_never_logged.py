# Section 2: Invariant — session string never appears in any log
# output at any level (DEBUG, INFO, WARNING, ERROR, CRITICAL).
# ---------------------------------------------------------------------------


class TestSessionStringNeverInLogs:
    """Pin the logging invariant. Any code path that logs the
    session string is a P0 security incident."""

    def _capture(self):
        """Context manager that captures every log record emitted
        at any level on the root logger for the duration of the
        with block. Returns the list of captured records."""
        from contextlib import contextmanager

        captured: list[logging.LogRecord] = []

        @contextmanager
        def _ctx():
            class _ListHandler(logging.Handler):
                def emit(self, record):
                    captured.append(record)

            handler = _ListHandler(level=logging.DEBUG)
            root = logging.getLogger()
            root.addHandler(handler)
            try:
                yield captured
            finally:
                root.removeHandler(handler)

        return _ctx()

    def test_log_warning_does_not_leak_session(self):
        """Force the plugin to emit a warning and assert the
        captured log records do not contain the session string."""
        with self._capture() as captured:
            logger = logging.getLogger("omi-telegram-user-account.test-logger")
            logger.warning(
                "Connection failed: %s",
                "fake error message — this would normally include a stack trace",
            )
            logger.error("RPC error: %s", "TimeoutError after 30s")
            logger.info("Backoff: %ds", 5)
            logger.debug("Internal state: %s", {"state": "ok", "retry": 0})

        for record in captured:
            self._assert_no_session_in_record(record)

    def test_log_with_session_in_argv_does_not_leak(self):
        """Common mistake: `logger.error("Failed to start: %s", exc)`
        where `exc` is the Telethon AuthKeyError, whose str()
        representation can include the session string. Pin that we
        redact before logging."""
        with self._capture() as captured:
            logger = logging.getLogger("omi-telegram-user-account.test-leak")

            # Simulate a Telethon exception that includes the session
            # string in its repr. This is the exact failure mode
            # we want to prevent.
            class _FakeTelethonError(Exception):
                def __str__(self):
                    return f"RPCError: auth key invalid for session {TEST_SESSION_STRING}"

            try:
                raise _FakeTelethonError()
            except _FakeTelethonError as exc:
                # The wrong way (which we want to be a regression):
                # logger.error("Connect failed: %s", exc)
                # The right way: use the PRODUCTION redaction helper
                # from redact.py. This test pins that the production
                # helper, not a local stub, is what protects the
                # log stream from session leaks. (Cubic review
                # 4614064929 P2.)
                from redact import redact_session_string

                safe_msg = redact_session_string(str(exc))
                logger.error("Connect failed: %s", safe_msg)

        for record in captured:
            self._assert_no_session_in_record(record)

    def test_repr_does_not_include_session(self):
        """The session string must not appear in the repr() of any
        plugin object. Belt-and-suspenders: if an exception bubbles
        up and gets printed via traceback.format_exception, the
        session must still be absent.
        """
        # This is a documentation test. The plugin's helpers and
        # exception types should never embed the session string in
        # their repr. We pin this by spot-checking: the plugin
        # module's source code should never contain the test
        # session string as a literal.
        # (If the plugin ever needs to embed the session in
        # exceptions — e.g., for internal debugging — it must use a
        # redacted form like "session=<redacted 64 chars>".)
        src_dir = Path(__file__).parent.parent  # plugins/telegram-user-account/
        for py in src_dir.glob("*.py"):
            content = py.read_text()
            assert TEST_SESSION_STRING not in content, (
                f"CRITICAL: test session string literal found in {py}. "
                f"This test file's TEST_SESSION_STRING must not appear in "
                f"production code — it indicates a copy/paste leak or a "
                f"test fixture hard-coded into a real source file."
            )

    def _assert_no_session_in_record(self, record):
        """Check a single LogRecord for any session leak."""
        # The message itself (after % substitution)
        msg = record.getMessage()
        assert TEST_SESSION_STRING not in msg, (
            f"CRITICAL: session string leaked into log message at " f"{record.levelname} on {record.name}: {msg!r}"
        )
        # The unformatted message
        assert TEST_SESSION_STRING not in record.msg, (
            f"CRITICAL: session string leaked into log msg-format on "
            f"{record.levelname} on {record.name}: {record.msg!r}"
        )
        # The args
        for arg in record.args or ():
            rendered = repr(arg)
            assert TEST_SESSION_STRING not in rendered, (
                f"CRITICAL: session string leaked into log arg on "
                f"{record.levelname} on {record.name}: arg={rendered!r}"
            )


