"""The most important test in this plugin.

The Telegram user-account integration authenticates as the user's
**personal account** via a Telethon session string. Anyone with that
string fully owns the user's Telegram identity — they can read all
chats, send as the user, and the only revocation path is Settings →
Devices on the user's phone.

This test file pins the absolute invariants:

1. The session string NEVER appears in any log output at any level.
2. The session string NEVER appears in the on-disk discovery file.
3. The session string NEVER appears in HTTP response bodies or
   exception messages.
4. The session string NEVER appears in the on-disk JSON storage.
5. The session string is masked in any display-only context (UI logs,
   banner strings, etc.).

A regression here is a security incident, not a bug. The test should
fail LOUDLY on any string presence, not warn.
"""

