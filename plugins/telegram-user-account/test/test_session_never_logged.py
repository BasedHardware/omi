# ---------------------------------------------------------------------------
# Section 1: Invariant — session string never written to the on-disk
# discovery file. The discovery file is the bridge between the
# plugin service and the desktop, and is read by the desktop's
# PluginDiscovery reader. A leaked session string in there = desktop
# compromise.
# ---------------------------------------------------------------------------


class TestDiscoveryFileNeverContainsSession:
    """P2 from the plan's safety model. The discovery file is the
    desktop-facing contract. The session string is high-stakes and
    must NEVER appear in it. (Plan §4: "api_id / api_hash /
    TELEGRAM_SESSION_STRING are never written to disk.")"""

    def test_session_string_absent_from_discovery_payload(self, tmp_path, monkeypatch):
        """Call write_discovery with the session string as one of the
        fields and assert the JSON written to disk does not contain
        the session string as a value."""
        # Import the discovery writer. This is the real function
        # (not a stub) so we can verify the on-disk invariant.
        from plugins._shared import plugin_discovery

        target = tmp_path / "ai-clone-telegram-user.json"
        # The discovery writer uses discovery_file(plugin_type) which
        # caches the per-plugin path in _DISCOVERY_FILES. Clear the
        # cache, then redirect the telegram-user entry to our tmp
        # path so the write lands in tmp_path instead of ~/.config.
        plugin_discovery._DISCOVERY_FILES.pop("telegram-user", None)
        plugin_discovery._DISCOVERY_FILES["telegram-user"] = target

        # write_discovery for user-account mode would carry account
        # metadata (phone, name) but NEVER the session string itself.
        # The actual signature is the same as the bot plugin's
        # write_discovery (no api_id/api_hash/session_string fields).
        plugin_discovery.write_discovery(
            plugin_url="http://127.0.0.1:18800",
            bearer_token="test-bearer-token",
            public_url=None,
            dev_mode=True,
            plugin_type="telegram-user",
        )

        raw = target.read_text()
        # Both the full session string and any prefix that uniquely
        # identifies it should be absent. We use the full string here
        # because the test is synthetic — in real usage the session
        # string is per-user and never appears in the plugin's code
        # at all, so any leak is by definition a regression.
        assert TEST_SESSION_STRING not in raw, (
            f"CRITICAL: session string leaked into discovery file at {target}.\n" f"File contents:\n{raw!r}"
        )
        # Also check the canonical Telethon session prefix "1A" which
        # every Telethon session string starts with — a substring
        # leak is just as bad as a full leak.
        assert "1AgAOMT" not in raw, (
            f"CRITICAL: Telethon session prefix leaked into discovery file at {target}.\n" f"File contents:\n{raw!r}"
        )

    def test_discovery_payload_keys_allowlisted(self, tmp_path, monkeypatch):
        """Even if a future change adds new fields, the discovery
        file's key set must remain a strict superset of the
        bot-plugin schema, with no long-lived credential fields.

        The whitelist: {version, instance_id, started_at, plugin_url,
        bearer_token, public_url, dev_mode, plugin_type,
        account_phone, account_name, device_label, omi_base_url}.
        Anything else = potential credential leak.
        """
        from plugins._shared import plugin_discovery

        target = tmp_path / "ai-clone-telegram-user.json"
        plugin_discovery._DISCOVERY_FILES.pop("telegram-user", None)
        plugin_discovery._DISCOVERY_FILES["telegram-user"] = target

        plugin_discovery.write_discovery(
            plugin_url="http://127.0.0.1:18800",
            bearer_token="test-bearer-token",
            public_url=None,
            dev_mode=True,
            plugin_type="telegram-user",
            omi_base_url="https://api.omi.me",
        )

        # Read back, get the keys, assert no credential fields.
        payload = json.loads(target.read_text())
        forbidden = {
            "session_string",
            "session",
            "telethon_session",
            "telegram_session",
            "api_id",
            "api_hash",
            "phone_auth_token",
            "auth_token",
        }
        present = set(payload.keys()) & forbidden
        assert not present, (
            f"CRITICAL: discovery file contains forbidden credential fields: {present}.\n" f"Full payload: {payload!r}"
        )

    def test_account_metadata_only_metadata(self, tmp_path, monkeypatch):
        """The discovery file may carry account metadata (phone
        number, display name) but those are recoverable from the
        Telethon session itself — they're not secret. Assert that
        account_phone and account_name are ALLOWED but session_string
        and api_hash are NOT, even if explicitly passed as kwargs.
        """
        from plugins._shared import plugin_discovery

        target = tmp_path / "ai-clone-telegram-user.json"
        plugin_discovery._DISCOVERY_FILES.pop("telegram-user", None)
        plugin_discovery._DISCOVERY_FILES["telegram-user"] = target

        # Force-pass the dangerous fields. The function should
        # silently drop them — but to do that, the function needs
        # to know about them. If a future writer doesn't know about
        # them, it might pass them through. This test pins the
        # strictest possible behavior: any field starting with
        # `session`, `api_`, or matching known credential patterns
        # must not appear in the on-disk JSON.
        #
        # NOTE: this test will pass even with the current writer
        # because it doesn't accept those kwargs. It will start
        # failing if a future change adds them.
        # If the API changes to accept them, this test must be
        # updated AND the writer must add an explicit allowlist.
        try:
            plugin_discovery.write_discovery(
                plugin_url="http://127.0.0.1:18800",
                bearer_token="test-bearer-token",
                public_url=None,
                dev_mode=True,
                plugin_type="telegram-user",
                # The fields below are HYPOTHETICAL. If a future
                # version of write_discovery accepts them, this test
                # will fail and require the writer to either reject
                # them or strip them from the on-disk JSON.
                session_string=TEST_SESSION_STRING,  # type: ignore[call-arg]
                api_hash="0123456789abcdef0123456789abcdef",  # type: ignore[call-arg]
                api_id=12345,  # type: ignore[call-arg]
            )
        except TypeError:
            # Current write_discovery doesn't accept these kwargs.
            # Good — this is the safer behavior. Skip the rest.
            pytest.skip("write_discovery doesn't accept credential kwargs — good.")

        raw = target.read_text()
        assert TEST_SESSION_STRING not in raw
        assert "0123456789abcdef0123456789abcdef" not in raw


