#!/usr/bin/env python3
"""Inventory Flutter REST schemas and backend OpenAPI coverage.

This is a progress tool for the schema single-source migration. It intentionally
uses static source scanning so it can run before the backend import harness or
Flutter toolchain is available.
"""

from __future__ import annotations

import argparse
import json
import re
from dataclasses import dataclass
from pathlib import Path
from typing import Any

ROOT_DIR = Path(__file__).resolve().parents[2]
APP_SCHEMA_DIR = ROOT_DIR / 'app' / 'lib' / 'backend' / 'schema'
APP_API_DIR = ROOT_DIR / 'app' / 'lib' / 'backend' / 'http' / 'api'
APP_MODELS_DIR = ROOT_DIR / 'app' / 'lib' / 'models'
APP_CLIENT_SPEC_PATH = ROOT_DIR / 'docs' / 'api-reference' / 'app-client-openapi.json'
MODEL_REST_DTO_FILES = (
    APP_MODELS_DIR / 'announcement.dart',
    APP_MODELS_DIR / 'subscription.dart',
    APP_MODELS_DIR / 'user_usage.dart',
)
LOCAL_NON_REST_SCHEMA_FILES = frozenset(
    {
        APP_SCHEMA_DIR / 'bt_device' / 'bt_device.dart',
    }
)

CLASS_RE = re.compile(r'^\s*class\s+([A-Za-z_][A-Za-z0-9_]*)\b', re.MULTILINE)
ENUM_RE = re.compile(r'^\s*enum\s+([A-Za-z_][A-Za-z0-9_]*)\b', re.MULTILINE)
FROM_JSON_RE = re.compile(r'\b(?:factory|static)?\s*([A-Za-z_][A-Za-z0-9_]*)?\.?fromJson\s*\(')
TO_JSON_RE = re.compile(r'\btoJson\s*\(')
INTERPOLATION_RE = re.compile(r'\$\{[^}]+\}|\$[A-Za-z_][A-Za-z0-9_]*')
OPENAPI_PARAM_RE = re.compile(r'\{[^}]+\}')
GENERATED_WIRE_RE = re.compile(r"""package:omi/backend/schema/gen/[^'"]+_wire\.g\.dart|wire\.Generated""")
GENERATED_MARKERS = (
    'Generated by',
    'JsonSerializableGenerator',
    'DO NOT EDIT',
)


@dataclass(frozen=True)
class DartSchemaFile:
    path: Path
    classes: list[str]
    enums: list[str]
    from_json_count: int
    to_json_count: int
    generated: bool
    generated_backed: bool

    def to_report(self) -> dict[str, Any]:
        return {
            'path': str(self.path.relative_to(ROOT_DIR)),
            'classes': self.classes,
            'enums': self.enums,
            'fromJson': self.from_json_count,
            'toJson': self.to_json_count,
            'generated': self.generated,
            'generated_backed': self.generated_backed,
        }


@dataclass(frozen=True)
class AppRoute:
    path: Path
    route: str
    normalized_route: str

    def to_report(self) -> dict[str, str]:
        return {
            'path': str(self.path.relative_to(ROOT_DIR)),
            'route': self.route,
            'normalized_route': self.normalized_route,
        }


@dataclass(frozen=True)
class OpenApiOperation:
    path: str
    normalized_path: str
    method: str
    operation_id: str
    unmodeled_success_response: bool

    def to_report(self) -> dict[str, Any]:
        return {
            'path': self.path,
            'normalized_path': self.normalized_path,
            'method': self.method,
            'operation_id': self.operation_id,
            'unmodeled_success_response': self.unmodeled_success_response,
        }


def scan_dart_schema_file(path: Path) -> DartSchemaFile:
    text = path.read_text()
    return DartSchemaFile(
        path=path,
        classes=CLASS_RE.findall(text),
        enums=ENUM_RE.findall(text),
        from_json_count=len(FROM_JSON_RE.findall(text)),
        to_json_count=len(TO_JSON_RE.findall(text)),
        generated=any(marker in text[:500] for marker in GENERATED_MARKERS) or path.name.endswith('.g.dart'),
        generated_backed=bool(GENERATED_WIRE_RE.search(text)),
    )


def scan_dart_schemas() -> list[DartSchemaFile]:
    return [
        scan_dart_schema_file(path)
        for path in sorted(APP_SCHEMA_DIR.rglob('*.dart'))
        if not path.name.endswith('.gen.dart')
    ]


def scan_rest_dto_files() -> list[DartSchemaFile]:
    paths = [*sorted(APP_SCHEMA_DIR.rglob('*.dart')), *sorted(APP_API_DIR.glob('*.dart'))]
    paths.extend(path for path in MODEL_REST_DTO_FILES if path.exists())
    return [
        scan_dart_schema_file(path)
        for path in paths
        if not path.name.endswith('.gen.dart')
        and not path.name.endswith('.g.dart')
        and path not in LOCAL_NON_REST_SCHEMA_FILES
    ]


def scan_local_non_rest_schema_files() -> list[DartSchemaFile]:
    return [scan_dart_schema_file(path) for path in sorted(LOCAL_NON_REST_SCHEMA_FILES) if path.exists()]


def normalize_app_route(route: str) -> str:
    route = INTERPOLATION_RE.sub('{param}', route)
    route = re.sub(r'(?<=[A-Za-z0-9_-])\{param\}$', '', route)
    route = route.split('?', 1)[0]
    route = re.sub(r'/+', '/', route)
    return '/' + route.lstrip('/')


def normalize_openapi_path(path: str) -> str:
    return OPENAPI_PARAM_RE.sub('{param}', path)


def route_prefix(route: str) -> str:
    parts = route.strip('/').split('/')
    if len(parts) >= 2:
        return '/' + '/'.join(parts[:2])
    if parts and parts[0]:
        return '/' + parts[0]
    return '/'


def _outer_quote_before(text: str, marker_start: int) -> str | None:
    for index in range(marker_start - 1, -1, -1):
        char = text[index]
        if char not in ("'", '"'):
            continue
        backslashes = 0
        cursor = index - 1
        while cursor >= 0 and text[cursor] == '\\':
            backslashes += 1
            cursor -= 1
        if backslashes % 2 == 0:
            return char
    return None


def _dart_string_tail_after_marker(text: str, marker_start: int, marker: str) -> str | None:
    quote = _outer_quote_before(text, marker_start)
    if quote is None:
        return None

    tail: list[str] = []
    cursor = marker_start + len(marker)
    if cursor < len(text) and text[cursor] == '}':
        cursor += 1
    interpolation_depth = 0
    while cursor < len(text):
        char = text[cursor]
        if interpolation_depth == 0 and char == quote:
            return ''.join(tail)
        if char == '$' and cursor + 1 < len(text) and text[cursor + 1] == '{':
            interpolation_depth += 1
            tail.append('${')
            cursor += 2
            continue
        if interpolation_depth > 0 and char in ("'", '"'):
            string_quote = char
            tail.append(char)
            cursor += 1
            while cursor < len(text):
                nested_char = text[cursor]
                tail.append(nested_char)
                cursor += 1
                if nested_char == '\\' and cursor < len(text):
                    tail.append(text[cursor])
                    cursor += 1
                    continue
                if nested_char == string_quote:
                    break
            continue
        if interpolation_depth > 0 and char == '{':
            interpolation_depth += 1
        elif interpolation_depth > 0 and char == '}':
            interpolation_depth -= 1
        tail.append(char)
        cursor += 1
    return None


def _scan_marker_routes(text: str, marker: str, *, must_start_with: str | None = None) -> list[str]:
    routes: list[str] = []
    for match in re.finditer(re.escape(marker), text):
        route = _dart_string_tail_after_marker(text, match.start(), marker)
        if route is None:
            continue
        if must_start_with is not None and not route.startswith(must_start_with):
            continue
        routes.append(route)
    return routes


def scan_app_routes() -> list[AppRoute]:
    routes: list[AppRoute] = []
    for path in sorted(APP_API_DIR.glob('*.dart')):
        text = path.read_text()
        env_routes = _scan_marker_routes(text, 'Env.apiBaseUrl', must_start_with='v')
        base_routes = [
            route
            for route in env_routes
            if re.search(rf"""_baseUrl\s*=\s*['"]\$\{{Env\.apiBaseUrl\}}{re.escape(route)}['"]""", text)
        ]
        for route in env_routes:
            routes.append(
                AppRoute(path=path, route='/' + route.lstrip('/'), normalized_route=normalize_app_route(route))
            )
        for base_route in base_routes:
            for local_route in _scan_marker_routes(text, '_baseUrl', must_start_with='/'):
                route = base_route.rstrip('/') + local_route
                routes.append(
                    AppRoute(path=path, route='/' + route.lstrip('/'), normalized_route=normalize_app_route(route))
                )

    unique: dict[tuple[Path, str], AppRoute] = {}
    for route in routes:
        unique[(route.path, route.normalized_route)] = route
    return sorted(unique.values(), key=lambda item: (str(item.path), item.normalized_route))


def load_openapi_schema_names(path: Path) -> list[str]:
    if not path.exists():
        return []
    spec = json.loads(path.read_text())
    return sorted(spec.get('components', {}).get('schemas', {}).keys())


def load_openapi_paths(path: Path) -> list[str]:
    if not path.exists():
        return []
    spec = json.loads(path.read_text())
    return sorted(spec.get('paths', {}).keys())


def is_unmodeled_success_schema(schema: dict[str, Any]) -> bool:
    if schema == {}:
        return True
    return (
        schema.get('type') == 'object'
        and not schema.get('properties')
        and schema.get('additionalProperties')
        in (
            None,
            True,
        )
    )


def operation_has_unmodeled_success_response(operation: dict[str, Any]) -> bool:
    for status_code, response in operation.get('responses', {}).items():
        if not status_code.startswith('2'):
            continue
        content = response.get('content') or {}
        json_content = content.get('application/json')
        if not json_content:
            continue
        schema = json_content.get('schema')
        if isinstance(schema, dict) and is_unmodeled_success_schema(schema):
            return True
    return False


def load_openapi_operations(path: Path) -> list[OpenApiOperation]:
    if not path.exists():
        return []
    spec = json.loads(path.read_text())
    operations = []
    for route, methods in spec.get('paths', {}).items():
        for method, operation in methods.items():
            operations.append(
                OpenApiOperation(
                    path=route,
                    normalized_path=normalize_openapi_path(route),
                    method=method.upper(),
                    operation_id=operation.get('operationId', ''),
                    unmodeled_success_response=operation_has_unmodeled_success_response(operation),
                )
            )
    return sorted(operations, key=lambda item: (item.path, item.method))


def build_report(spec_path: Path) -> dict[str, Any]:
    dart_files = scan_rest_dto_files()
    local_non_rest_files = scan_local_non_rest_schema_files()
    manual_files = [
        item for item in dart_files if not item.generated and (item.from_json_count > 0 or item.to_json_count > 0)
    ]
    generated_backed_files = [item for item in manual_files if item.generated_backed]
    remaining_manual_files = [item for item in manual_files if not item.generated_backed]
    app_routes = scan_app_routes()
    openapi_paths = load_openapi_paths(spec_path)
    openapi_operations = load_openapi_operations(spec_path)
    openapi_prefixes = sorted({route_prefix(path) for path in openapi_paths})
    app_route_prefixes = sorted({route_prefix(route.normalized_route) for route in app_routes})
    uncovered_prefixes = sorted(set(app_route_prefixes) - set(openapi_prefixes))
    app_route_paths = {route.normalized_route for route in app_routes}
    app_used_unmodeled_operations = [
        operation
        for operation in openapi_operations
        if operation.normalized_path in app_route_paths and operation.unmodeled_success_response
    ]
    return {
        'dart_schema_dirs': [
            str(APP_SCHEMA_DIR.relative_to(ROOT_DIR)),
            str(APP_API_DIR.relative_to(ROOT_DIR)),
            *(str(path.relative_to(ROOT_DIR)) for path in MODEL_REST_DTO_FILES if path.exists()),
        ],
        'app_client_openapi': str(spec_path.relative_to(ROOT_DIR)),
        'openapi_schema_count': len(load_openapi_schema_names(spec_path)),
        'openapi_schemas': load_openapi_schema_names(spec_path),
        'openapi_path_count': len(openapi_paths),
        'openapi_paths': openapi_paths,
        'openapi_operations': [operation.to_report() for operation in openapi_operations],
        'app_used_unmodeled_success_response_count': len(app_used_unmodeled_operations),
        'app_used_unmodeled_success_responses': [operation.to_report() for operation in app_used_unmodeled_operations],
        'app_route_count': len(app_routes),
        'app_routes': [route.to_report() for route in app_routes],
        'app_route_prefixes': app_route_prefixes,
        'openapi_prefixes': openapi_prefixes,
        'uncovered_app_route_prefixes': uncovered_prefixes,
        'rest_dto_file_count': len(dart_files),
        'manual_dart_json_schema_file_count': len(manual_files),
        'manual_dart_json_schema_files': [item.to_report() for item in manual_files],
        'generated_backed_adapter_file_count': len(generated_backed_files),
        'generated_backed_adapter_files': [item.to_report() for item in generated_backed_files],
        'remaining_manual_dart_json_schema_file_count': len(remaining_manual_files),
        'remaining_manual_dart_json_schema_files': [item.to_report() for item in remaining_manual_files],
        'local_non_rest_schema_file_count': len(local_non_rest_files),
        'local_non_rest_schema_files': [item.to_report() for item in local_non_rest_files],
    }


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description='Inventory app-client schema migration coverage.')
    parser.add_argument(
        '--spec',
        default=str(APP_CLIENT_SPEC_PATH),
        help='app-client OpenAPI spec path; defaults to docs/api-reference/app-client-openapi.json',
    )
    parser.add_argument('--json', action='store_true', help='emit machine-readable JSON')
    parser.add_argument(
        '--fail-on-uncovered-prefixes',
        action='store_true',
        help='fail when Flutter REST route prefixes are missing from app-client OpenAPI',
    )
    parser.add_argument(
        '--fail-on-manual-rest-dtos',
        action='store_true',
        help='fail when raw hand-written REST DTO JSON parsing remains',
    )
    parser.add_argument(
        '--fail-on-unmodeled-openapi-object-response',
        action='store_true',
        help='fail when app-used OpenAPI operations expose untyped object/empty JSON success responses',
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    report = build_report(Path(args.spec))
    if args.fail_on_uncovered_prefixes and report['uncovered_app_route_prefixes']:
        print(
            'Uncovered Flutter REST route prefixes: ' + ', '.join(report['uncovered_app_route_prefixes']),
            flush=True,
        )
        return 1
    if args.fail_on_manual_rest_dtos and report['remaining_manual_dart_json_schema_files']:
        paths = [item['path'] for item in report['remaining_manual_dart_json_schema_files']]
        print('Remaining raw manual Dart JSON schema files: ' + ', '.join(paths), flush=True)
        return 1
    if args.fail_on_unmodeled_openapi_object_response and report['app_used_unmodeled_success_responses']:
        operations = [
            f"{item['method']} {item['path']} ({item['operation_id']})"
            for item in report['app_used_unmodeled_success_responses']
        ]
        print('App-used unmodeled OpenAPI success responses: ' + ', '.join(operations), flush=True)
        return 1
    if args.json:
        print(json.dumps(report, indent=2, sort_keys=True))
        return 0

    print(f"REST DTO candidate files: {report['rest_dto_file_count']}")
    print(f"Manual Dart JSON schema files: {report['manual_dart_json_schema_file_count']}")
    print(f"Generated-backed adapter files: {report['generated_backed_adapter_file_count']}")
    print(f"Remaining raw manual Dart JSON schema files: {report['remaining_manual_dart_json_schema_file_count']}")
    print(f"Local non-REST schema files: {report['local_non_rest_schema_file_count']}")
    print(f"OpenAPI schemas in app-client spec: {report['openapi_schema_count']}")
    print(f"OpenAPI paths in app-client spec: {report['openapi_path_count']}")
    print(f"App-used unmodeled OpenAPI success responses: {report['app_used_unmodeled_success_response_count']}")
    print(f"Flutter REST routes found: {report['app_route_count']}")
    if report['uncovered_app_route_prefixes']:
        print('Uncovered Flutter REST route prefixes: ' + ', '.join(report['uncovered_app_route_prefixes']))
    for item in report['app_used_unmodeled_success_responses']:
        print(f"- unmodeled {item['method']} {item['path']} ({item['operation_id']})")
    for item in report['remaining_manual_dart_json_schema_files']:
        classes = ', '.join(item['classes']) or '(no classes found)'
        print(f"- {item['path']}: {classes} (fromJson={item['fromJson']}, toJson={item['toJson']})")
    for item in report['local_non_rest_schema_files']:
        classes = ', '.join(item['classes']) or '(no classes found)'
        print(f"- local {item['path']}: {classes} (fromJson={item['fromJson']}, toJson={item['toJson']})")
    return 0


if __name__ == '__main__':
    raise SystemExit(main())
