#!/usr/bin/env python3
"""Generate Swift Codable DTOs from an OpenAPI schema.

Mirrors backend/scripts/generate_ts_openapi_types.py but emits Swift. Output is
pure wire-shape DTOs (snake_case CodingKeys, no behavior) that the desktop's
domain models adapt. The app-client OpenAPI spec is the schema authority.

Generated DTOs use `decodeIfPresent` for optional fields and tolerate unknown
keys (the backend may add fields), so the desktop's tolerant domain-model
defaults are preserved by adapting from the generated wire type.
"""

from __future__ import annotations

import argparse
import json
import re
import sys
from pathlib import Path
from typing import Any

ROOT_DIR = Path(__file__).resolve().parents[2]
DEFAULT_SPEC_PATH = ROOT_DIR / 'docs' / 'api-reference' / 'app-client-openapi.json'
DEFAULT_OUTPUT = ROOT_DIR / 'desktop' / 'macos' / 'Desktop' / 'Sources' / 'Generated' / 'OmiApi.generated.swift'

# Schemas to generate. Keep this the high-traffic desktop read surface; expand
# as the desktop Codable migration progresses. Each entry pulls in transitive
# $ref dependencies automatically.
