def _swift_property(swift_name: str, wire_name: str, type_expr: str, optional: bool) -> str:
    decl = f'public let {swift_name}: {type_expr}' + ('?' if optional else '')
    if swift_name == wire_name:
        return f'    {decl}'
    return f'    {decl}\n    // wire: "{wire_name}"'


def _render_struct(name: str, schema: dict[str, Any]) -> str:
    props = schema.get('properties') or {}
    required = set(schema.get('required') or [])

    # Build CodingKeys + decode body.
    fields: list[tuple[str, str, str, bool]] = []  # (swift_name, wire_name, type, optional)
    for wire_name, prop_schema in sorted(props.items()):
        swift_name = _swift_prop_name(wire_name)
        type_expr, optional = _swift_type(prop_schema, required=wire_name in required)
        fields.append((swift_name, wire_name, type_expr, optional))

    lines = [f'public struct {name}: Codable {{']
    for swift_name, wire_name, type_expr, optional in fields:
        decl = f'  public let {swift_name}: {type_expr}' + ('?' if optional else '')
        lines.append(decl)
    lines.append('')

    # CodingKeys
    needs_coding_keys = any(sn != wn for sn, wn, _, _ in fields)
    if needs_coding_keys:
        lines.append('  private enum CodingKeys: String, CodingKey {')
        for swift_name, wire_name, _, _ in fields:
            if swift_name == wire_name:
                lines.append(f'    case {swift_name}')
            else:
                lines.append(f'    case {swift_name} = "{wire_name}"')
        lines.append('  }')
        lines.append('')

    # init(from:) using decodeIfPresent for optionals + tolerant defaults.
    lines.append('  public init(from decoder: Decoder) throws {')
    lines.append('    let c = try decoder.container(keyedBy: CodingKeys.self)')
    for swift_name, wire_name, type_expr, optional in fields:
        if optional:
            lines.append(f'    {swift_name} = try c.decodeIfPresent({type_expr}.self, forKey: .{swift_name})')
        else:
            lines.append(f'    {swift_name} = try c.decode({type_expr}.self, forKey: .{swift_name})')
    lines.append('  }')
    lines.append('')

    # Memberwise init for adapter construction.
    param_list = ', '.join(f'{sn}: {te}' + ('?' if opt else '') for sn, _, te, opt in fields)
    lines.append(f'  public init({param_list}) {{')
    for swift_name, _, _, _ in fields:
        lines.append(f'    self.{swift_name} = {swift_name}')
    lines.append('  }')
    lines.append('}')
    return '\n'.join(lines) + '\n\n'


def _swift_prop_name(wire_name: str) -> str:
    parts = re.split(r'[_\W]+', wire_name)
    if not parts:
        return wire_name
    first = parts[0]
    rest = ''.join(p[:1].upper() + p[1:] for p in parts[1:])
    candidate = first + rest
    if candidate in SWIFT_RESERVED:
        candidate = candidate + '_'
    return candidate


def _collect_deps(schemas: dict[str, Any], targets: tuple[str, ...]) -> list[str]:
    """Topo-collect target schemas + transitive $ref deps, sorted alphabetically."""
    seen: set[str] = set()
    order: list[str] = []

    def visit(name: str) -> None:
        if name in seen or name not in schemas:
            return
        seen.add(name)
        schema = schemas[name]
        for prop in (schema.get('properties') or {}).values():
            _visit_refs(prop, visit)
        if 'items' in schema:
            _visit_refs(schema['items'], visit)
        for part in schema.get('anyOf', []) or schema.get('oneOf', []) or []:
            _visit_refs(part, visit)
        order.append(name)

    def _visit_refs(node: Any, visit) -> None:
        if isinstance(node, dict):
            if '$ref' in node:
                visit(_ref_name(node['$ref']))
            for v in node.values():
                _visit_refs(v, visit)
        elif isinstance(node, list):
            for v in node:
                _visit_refs(v, visit)

    for t in targets:
        visit(t)
    return sorted(set(order))


def generate(spec: dict[str, Any], spec_path: str) -> str:
    schemas = spec.get('components', {}).get('schemas', {})
    targets = tuple(t for t in TARGET_SCHEMAS if t in schemas)
    names = _collect_deps(schemas, targets)

    out = [HEADER_PRE.format(spec_path=spec_path)]
    # OmiAnyCodable helper for opaque JSON values — module scope, outside OmiAPI.
    out.append(ANY_CODABLE)
    out.append(HEADER_NAMESPACE_OPEN)
    for name in names:
        schema = schemas[name]
        block = ''
        if schema.get('type') == 'string' and 'enum' in schema:
            block = _render_enum(name, schema)
        elif schema.get('type') == 'object' or 'properties' in schema:
            block = _render_struct(name, schema)
        else:
            continue
        # Indent the block by 2 spaces to nest inside `enum OmiAPI`.
        indented = '\n'.join(('  ' + line) if line else line for line in block.split('\n'))
        out.append(indented + '\n')
    out.append(FOOTER)
    return ''.join(out)


ANY_CODABLE = '''/// Type-erased Codable for opaque backend JSON values (open maps, anyOf unions
/// the generator does not narrow further). Round-trips through JSONSerialization.
public struct OmiAnyCodable: Codable, Equatable {
  public let value: Any

  public init(_ value: Any) { self.value = value }

  public init(from decoder: Decoder) throws {
    let c = try decoder.singleValueContainer()
    if c.decodeNil() { value = NSNull() }
    else if let v = try? c.decode(Bool.self) { value = v }
    else if let v = try? c.decode(Double.self) { value = v }
    else if let v = try? c.decode(String.self) { value = v }
    else if let v = try? c.decode([OmiAnyCodable].self) { value = v.map { $0.value } }
    else if let v = try? c.decode([String: OmiAnyCodable].self) { value = v.mapValues { $0.value } }
    else { value = NSNull() }
  }

  public func encode(to encoder: Encoder) throws {
    var c = encoder.singleValueContainer()
    switch value {
    case is NSNull: try c.encodeNil()
    case let v as Bool: try c.encode(v)
    case let v as Double: try c.encode(v)
    case let v as Int: try c.encode(v)
    case let v as String: try c.encode(v)
    case let v as [Any]: try c.encode(v.map { OmiAnyCodable($0) })
    case let v as [String: Any]: try c.encode(v.mapValues { OmiAnyCodable($0) })
    default: try c.encodeNil()
    }
  }

  public static func == (lhs: OmiAnyCodable, rhs: OmiAnyCodable) -> Bool {
    let lhsData = try? JSONSerialization.data(withJSONObject: [lhs.value], options: [.sortedKeys])
    let rhsData = try? JSONSerialization.data(withJSONObject: [rhs.value], options: [.sortedKeys])
    return lhsData == rhsData
  }
}

'''


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument('--spec', type=Path, default=DEFAULT_SPEC_PATH)
    p.add_argument('--output', type=Path, default=DEFAULT_OUTPUT)
    p.add_argument('--check', action='store_true', help='verify generated file is fresh')
    return p.parse_args()


def main() -> int:
    args = parse_args()
    spec = json.loads(args.spec.read_text())
    generated = generate(spec, str(args.spec.relative_to(ROOT_DIR)) if args.spec.is_absolute() else str(args.spec))

    if args.check:
        existing = args.output.read_text() if args.output.exists() else ''
        if existing != generated:
            rel = args.output.relative_to(ROOT_DIR) if args.output.is_absolute() else args.output
            print(f'{rel} is out of date. Run: python {Path(__file__).name}', file=sys.stderr)
            return 1
        return 0

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(generated)
    print(f'wrote {args.output}')
    return 0


if __name__ == '__main__':
    raise SystemExit(main())
