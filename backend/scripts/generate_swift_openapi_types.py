HEADER_PRE = """// GENERATED CODE - DO NOT EDIT.
// Generated by backend/scripts/generate_swift_openapi_types.py from {spec_path}
// Swift wire DTOs for the desktop app's backend REST surface. Domain models
// (ServerConversation, ServerMemory, Goal, ActionItem) adapt from these types;
// they do not replace them wholesale. All generated types are namespaced under
// `OmiAPI` so they don't collide with the domain models in APIClient.swift.

import Foundation

"""


HEADER_NAMESPACE_OPEN = """
/// Namespace for generated backend REST wire DTOs. Domain models in
/// APIClient.swift adapt from `OmiAPI.*` types; consumers should not construct
/// these directly outside the adapter layer.
public enum OmiAPI {

"""


FOOTER = "}\n"


def _ref_name(ref: str) -> str:
    return ref.rsplit('/', 1)[-1]


def _swift_type(schema: dict[str, Any], required: bool = False) -> tuple[str, bool]:
    """Return (swift type expression, is_optional).

    Tolerant decoding: optional unless the field is required AND has a non-null
    primitive shape. Refs to generated structs are decodeIfPresent-tolerant.
    """
    if '$ref' in schema:
        name = _ref_name(schema['$ref'])
        return name, not required
    if 'anyOf' in schema or 'oneOf' in schema:
        parts = schema.get('anyOf') or schema.get('oneOf') or []
        non_null = [p for p in parts if p.get('type') != 'null']
        if len(non_null) == 1 and len(non_null) != len(parts):
            inner, _ = _swift_type(non_null[0], required=True)
            return inner, True
        return 'OmiAnyCodable', not required
    if 'type' not in schema:
        return 'OmiAnyCodable', not required
    t = schema['type']
    if t == 'string':
        return 'String', not required
    if t == 'integer':
        if schema.get('format') in ('int32', 'int64'):
            return 'Int', not required
        return 'Int', not required
    if t == 'number':
        return 'Double', not required
    if t == 'boolean':
        return 'Bool', not required
    if t == 'array':
        items = schema.get('items', {})
        inner, _ = _swift_type(items, required=True)
        return f'[{inner}]', not required
    if t == 'object':
        ap = schema.get('additionalProperties')
        if isinstance(ap, dict):
            val_type, _ = _swift_type(ap, required=True)
            return f'[String: {val_type}]', not required
        return '[String: OmiAnyCodable]', not required
    return 'OmiAnyCodable', not required


def _render_enum(name: str, schema: dict[str, Any]) -> str:
    values = schema.get('enum') or []
    lines = [f'public enum {name}: String, Codable, CaseIterable {{']
    for v in values:
        if re.match(r'^[A-Za-z_][A-Za-z0-9_]*$', v):
            case_name = v if v not in SWIFT_RESERVED else v + '_'
            if case_name == v:
                lines.append(f'  case {v}')
            else:
                lines.append(f'  case {case_name} = "{v}"')
        else:
            lines.append(f'  case {re.sub(r"[^A-Za-z0-9_]", "_", v)} = "{v}"')
    # Tolerant fallback: unknown enum values decode to a synthetic case rather
    # than throwing, preserving the desktop's tolerant parsing.
    lines.append('  case _unknown = "__unknown__"')
    lines.append('  public init(from decoder: Decoder) throws {')
    lines.append('    let c = try decoder.singleValueContainer()')
    lines.append(f'    let raw = try c.decode(String.self)')
    lines.append(f'    self = {name}(rawValue: raw) ?? ._unknown')
    lines.append('  }')
    lines.append('}')
    return '\n'.join(lines) + '\n\n'


