#!/usr/bin/env bash
# generate_ui_flow_index.sh
# Scans lib/pages/** for page classes and regenerates ui-flow.screens.yaml.
#
# Usage (from repo root):
#   bash app/scripts/agent/generate_ui_flow_index.sh
#
# Output:
#   app/docs/flows/generated/ui-flow.screens.yaml
#
# This script is intentionally simple and grep-based so it runs with zero
# extra dependencies in CI and local environments.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
APP_DIR="$(cd "${SCRIPT_DIR}/../.." && pwd)"
LIB_DIR="${APP_DIR}/lib"
OUT_FILE="${APP_DIR}/docs/flows/generated/ui-flow.screens.yaml"

echo "→ Scanning ${LIB_DIR}/pages for page classes..."

# ── Header ────────────────────────────────────────────────────────────────────
cat > "${OUT_FILE}" <<YAML
# Auto-generated by scripts/agent/generate_ui_flow_index.sh
# DO NOT EDIT MANUALLY — regenerate with: bash scripts/agent/generate_ui_flow_index.sh
# Generated: $(date -u +"%Y-%m-%dT%H:%M:%SZ")

meta:
  generated_by: generate_ui_flow_index.sh
  generated_at: $(date -u +"%Y-%m-%dT%H:%M:%SZ")

screens:
YAML

# ── Scan lib/pages/** for StatelessWidget / StatefulWidget / ConsumerWidget ──
find "${LIB_DIR}/pages" -name "*.dart" | sort | while read -r file; do
  # Extract the first public class that extends a Widget type
  class_name=$(grep -oP 'class \K\w+(?=\s+extends\s+(Stateless|Stateful|Consumer|Hooks)Widget)' "${file}" | head -1 || true)
  if [[ -z "${class_name}" ]]; then
    continue
  fi

  # Derive relative path from lib/
  rel_path="lib/${file#"${LIB_DIR}/"}"

  # Derive flow from directory structure (first subdir under pages/)
  flow=$(echo "${rel_path}" | sed 's|lib/pages/||' | cut -d'/' -f1)

  cat >> "${OUT_FILE}" <<YAML
  - class: ${class_name}
    file: ${rel_path}
    flow: ${flow}
YAML
done

echo "✓ Written: ${OUT_FILE}"
echo ""
echo "Next step: review the output and update app/docs/flows/ui-flow.md if screen names"
echo "or navigation changed. Commit both files together."
