#!/usr/bin/env bash
# generate_data_flow_inventory.sh
# Scans lib/backend/http/api/** and lib/services/sockets/** to regenerate
# data-flow.inventory.yaml.
#
# Usage (from repo root):
#   bash app/scripts/agent/generate_data_flow_inventory.sh
#
# Output:
#   app/docs/flows/generated/data-flow.inventory.yaml

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
APP_DIR="$(cd "${SCRIPT_DIR}/../.." && pwd)"
LIB_DIR="${APP_DIR}/lib"
OUT_FILE="${APP_DIR}/docs/flows/generated/data-flow.inventory.yaml"

echo "→ Scanning API modules and WebSocket services..."

# ── Header ────────────────────────────────────────────────────────────────────
cat > "${OUT_FILE}" <<YAML
# Auto-generated by scripts/agent/generate_data_flow_inventory.sh
# DO NOT EDIT MANUALLY — regenerate with: bash scripts/agent/generate_data_flow_inventory.sh
# Generated: $(date -u +"%Y-%m-%dT%H:%M:%SZ")

meta:
  generated_by: generate_data_flow_inventory.sh
  generated_at: $(date -u +"%Y-%m-%dT%H:%M:%SZ")

api_modules:
YAML

# ── Scan lib/backend/http/api/*.dart ─────────────────────────────────────────
API_DIR="${LIB_DIR}/backend/http/api"
if [[ -d "${API_DIR}" ]]; then
  find "${API_DIR}" -name "*.dart" ! -name "_*" | sort | while read -r file; do
    rel="lib/${file#"${LIB_DIR}/"}"
    module=$(basename "${file}" .dart)
    echo "  - module: ${module}" >> "${OUT_FILE}"
    echo "    file: ${rel}" >> "${OUT_FILE}"
  done
else
  echo "  # WARNING: ${API_DIR} not found — run script from repo root" >> "${OUT_FILE}"
fi

# ── Endpoint path patterns (grep for quoted /v1/ paths) ──────────────────────
echo "" >> "${OUT_FILE}"
echo "endpoint_path_patterns:" >> "${OUT_FILE}"

if [[ -d "${API_DIR}" ]]; then
  grep -rhoP '(?<=["'"'"'])/v1/[^"'"'"' )]+' "${API_DIR}" 2>/dev/null \
    | sort -u \
    | while read -r path; do
        echo "  - ${path}" >> "${OUT_FILE}"
      done
fi

# ── WebSocket event types ─────────────────────────────────────────────────────
echo "" >> "${OUT_FILE}"
echo "websocket_event_types:" >> "${OUT_FILE}"

SCHEMA_DIR="${LIB_DIR}/backend/schema"
if [[ -d "${SCHEMA_DIR}" ]]; then
  grep -rhoP "MessageEventType\.\K\w+" "${LIB_DIR}" 2>/dev/null \
    | sort -u \
    | while read -r evt; do
        echo "  - ${evt}" >> "${OUT_FILE}"
      done
fi

echo "✓ Written: ${OUT_FILE}"
echo ""
echo "Next step: review the output and update app/docs/flows/data-flow.md if API modules"
echo "or WebSocket events changed. Commit both files together."
